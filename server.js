// ─── 日次ユーザー制限 ───────────────────────────────────────────
const DAILY_LIMIT = 10
const USAGE_FILE = path.join(__dirname, 'data', 'usage.json')

function loadUsage() {
  try {
    return JSON.parse(fs.readFileSync(USAGE_FILE, 'utf-8'))
  } catch {
    return {}
  }
}

function saveUsage(data) {
  fs.writeFileSync(USAGE_FILE, JSON.stringify(data, null, 2))
}

function getTodayJST() {
  return new Date().toLocaleDateString('ja-JP', {
    timeZone: 'Asia/Tokyo',
    year: 'numeric', month: '2-digit', day: '2-digit'
  }).replace(/\//g, '-')
}

function checkAndRegisterSession(sessionId) {
  const usage = loadUsage()
  const today = getTodayJST()
  if (!usage[today]) usage[today] = []
  if (usage[today].includes(sessionId)) return { ok: true, count: usage[today].length }
  if (usage[today].length >= DAILY_LIMIT) return { ok: false, count: usage[today].length }
  usage[today].push(sessionId)
  saveUsage(usage)
  return { ok: true, count: usage[today].length }
}

// ─── ブースデータ（data/exhibitors.json から動的ロード）────────
const EXHIBITORS_FILE = path.join(__dirname, 'data', 'exhibitors.json')

function loadBoothData() {
  try {
    const raw = JSON.parse(fs.readFileSync(EXHIBITORS_FILE, 'utf-8'))
    const lines = [
      `【クリエイターEXPO 2026 公式出展者一覧 — ${raw.category}】`,
      `取得日時: ${new Date(raw.fetchedAt).toLocaleString('ja-JP', { timeZone: 'Asia/Tokyo' })} / ${raw.count}名`,
      '',
    ]
    raw.exhibitors.forEach(e => {
      const sub   = e.sub   ? `[${e.sub}]`   : ''
      const stand = e.stand ? `[${e.stand}]` : ''
      lines.push(`${e.no}. ${e.name} ${stand}${sub}${e.desc ? ` — ${e.desc}` : ''}`)
    })
    return lines.join('\n')
  } catch {
    return '（出展者データ未取得 — fetch-exhibitors.js を実行してください）'
  }
}

function buildExhibitorMap() {
  try {
    const raw = JSON.parse(fs.readFileSync(EXHIBITORS_FILE, 'utf-8'))
    const map = {}
    raw.exhibitors.forEach(e => {
      map[e.name] = { ctUrl: e.ctUrl || '', stand: e.stand || '' }
    })
    return map
  } catch {
    return {}
  }
}

